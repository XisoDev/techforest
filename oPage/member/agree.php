			② 회사는 1항에 의한 서비스 이용정지 30일 전 푸쉬기능 혹인 이메일을 통하여 서비스 이용정지에 대하여 안내하고, 서비스 이용정지가 되는 경우 다시 푸쉬기능 혹은 이메일을 통하여 서비스 이용정지 사실에
