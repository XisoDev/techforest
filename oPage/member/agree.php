			② 회사는 제공하는 서비스와 관련되는 정보 또는 광고를 서비스 화면, 홈페이지 등에 게재할 수 있으며, 회원들에게 전자우편 및 SMS, MMS, 푸쉬, 알림 기능을 통해 알릴 수 있다. <br />
